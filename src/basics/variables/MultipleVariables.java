package basics.variables;

public class MultipleVariables {

	public static void main(String[] args) {
		int x = 5;
		int y = 6;
		int z = 7;
		int a = 5, b = 6, c = 50;
		//One value to multiple variables
		int d, e, f;
		d = e = f = 50;
		
		System.out.println(x + y + z);
		System.out.println(a + b + c);
		System.out.println(d + e + f);

	}

}
