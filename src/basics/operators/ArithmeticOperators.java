package basics.operators;

public class ArithmeticOperators {

	public static void main(String[] args) {
			
		double x = 5, y = 4, addition, subtraction, multiplication, division, modulus, increment, decrement;
		
		addition = x + y;
		subtraction = x - y;
		multiplication = x * y;
		division = x / y;
		modulus = x % y;
		increment = ++x;
		decrement = --x;
		
		System.out.println("The addition is: " + addition);
		System.out.println("The subtraction is: " + subtraction);
		System.out.println("The multiplication is: " + multiplication);
		System.out.println("The division is: " + division);
		System.out.println("The modulus is: " + modulus);
		System.out.println("The increment is: " + increment);
		System.out.println("The decrement is: " + decrement);
		

	}

}
